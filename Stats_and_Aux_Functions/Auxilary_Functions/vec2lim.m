function out = vec2lim( vec )
% VEC2LIM( vec ) takes a vector and returns a vector with the first and last 
% entries
%--------------------------------------------------------------------------
% ARGUMENTS
% Mandatory
% Optional
%--------------------------------------------------------------------------
% OUTPUT
% 
%--------------------------------------------------------------------------
% EXAMPLES
% 
%--------------------------------------------------------------------------
