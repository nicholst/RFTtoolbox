function sigma = FWHM2sigma( FWHM )
% FWHM2sigma( FWHM ) converts the FWHM to std deviation
%--------------------------------------------------------------------------
% ARGUMENTS
% 
%--------------------------------------------------------------------------
% OUTPUT
% 
%--------------------------------------------------------------------------
% EXAMPLES
% 
%--------------------------------------------------------------------------
% AUTHOR: Samuel Davenport.

sigma = FWHM/sqrt(8*log(2));
end

